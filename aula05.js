/*
Operadores Básicos
Estes executam as operações fundamentais da matemática:
+ (adição): Soma dois valores, como 5 + 3 resulta em 8.
- (subtração): Subtrai o segundo valor do primeiro, como 5 - 3 resulta em 2.
* (multiplicação): Multiplica valores, como 5 * 3 resulta em 15.
/ (divisão): Divide o primeiro pelo segundo, como 6 / 2 resulta em 3.

Operadores de Resto e Potência
% (módulo ou resto): Retorna o resto da divisão, como 10 % 3 resulta em 1.
** (exponenciação): Eleva à potência, como 2 ** 3 resulta em 8.
Operadores Unários
São aplicados a um único operando:
++ (incremento): Aumenta o valor em 1 (x++ ou ++x).
-- (decremento): Diminui o valor em 1 (x-- ou --x).
+ (unário positivo): Converte para número positivo.
- (unário negativo): Converte para número negativo.
*/

